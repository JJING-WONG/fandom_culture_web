export default function ArchiveDetailPage() {
  const { season, epId } = useParams();
  const navigate = useNavigate();

  // 🔵 안전하게 숫자 처리
  const s = Number(season);
  const e = Number(epId);

  const seasonData = archiveDetails[s];
  const episode = seasonData ? seasonData[e] : null;

  if (!episode) {
    return (
      <>
        <Header />
        <PageContainer>
          <h2>해당 기록을 찾을 수 없습니다.</h2>
          <BackButton onClick={() => navigate("/archive")}>
            뒤로가기
          </BackButton>
        </PageContainer>
        <Footer />
      </>
    );
  }

  return (
    <>
      <Header />

      <PageContainer>
        <Title>{episode.title}</Title>

        <InfoBox>
          <InfoItem>
            <strong>시즌:</strong> {s}
          </InfoItem>
          <InfoItem>
            <strong>회차:</strong> {e}화
          </InfoItem>
        </InfoBox>

        <Section>
          <SectionTitle>사건 요약</SectionTitle>
          <ContentBox>{episode.summary}</ContentBox>
        </Section>

        <Section>
          <SectionTitle>주요 단서</SectionTitle>
          <ContentBox>
            <ClueList>
              {episode.clues[0] === "X" ? (
                <ClueItem>등록된 단서 없음</ClueItem>
              ) : (
                episode.clues.map((clue, i) => (
                  <ClueItem key={i}>{clue}</ClueItem>
                ))
              )}
            </ClueList>
          </ContentBox>
        </Section>

        <BackButton onClick={() => navigate("/archive")}>
          기록 목록으로 돌아가기
        </BackButton>
      </PageContainer>

      <Footer />
    </>
  );
}